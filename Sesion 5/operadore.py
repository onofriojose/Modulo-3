#evaludar True or False
print("Operadores Logicos")
a = True
b = False
print(f"a = {a} y b = {b}")
print(f"a and b son: {a and b}") #False, deben cumplirse las 2 condiciones
print(f"a or b son: {a or b}") #True, debe cumplirse una de las 2 condiciones
print(f"not a es {not a}")#False
print(f"not b es {not b}")#True
print("----------------------------------------")

#Operadores de comparacion
c = 10
d = 5
print("Operadores de comparacion")
print(f"c={c} > d={d} = {c>d}") #True, c mayor que d
print(f"c={c} < d={d} = {c<d}") #False, c menor que d
print(f"c={c} >= d={d} = {c>=d}") #True, c mayor o igual que d
print(f"c={c} <= d={d} = {c<=d}") #False. c menor o igual que d
print(f"c={c} == d={d} = {c==d}") #False, igual que 
print(f"c={c} != d={d} = {c!=d}") #True, distinto que